#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*str;

	i = 0;
	str = (char*)s;
	while (str[i])
	{
		if (str[i] == (char)c)
			return ((char*)s + i + 1);
		i++;
	}
	if ((char)c == '\0')
		return ((char*)s + i);
	return (0);
}
